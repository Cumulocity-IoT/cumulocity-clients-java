package com.cumulocity.generic.mqtt;

public enum PayloadFormatIndicator {

    /**
     * The format of the bytes of the payload is not known.
     *
     * @since 4.0.0, CE 2019.1
     */
    UNSPECIFIED,
    /**
     * The format of the bytes of the payload is UTF-8.
     *
     * @since 4.0.0, CE 2019.1
     */
    UTF_8
}
